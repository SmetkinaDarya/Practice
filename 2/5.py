# 1 вариант
# Ввод начального пробега x и целевой дистанции y
x = float(input("Введите начальный пробег спортсмена (в километрах): "))
y = float(input("Введите целевую дистанцию (в километрах): "))

# Инициализация счетчика дней
days = 1

# Цикл для увеличения пробега и подсчета дней
while x < y:
    x += x * 0.1  # Увеличиваем пробег на 10%
    days += 1

# Вывод номера дня, на который пробег составит не менее y километров
print("На {}-й день спортсмен достигнет или превысит целевую дистанцию {} километров.".format(days, y))